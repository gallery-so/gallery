const NOTION_DOCS_URL =
  'https://gallery-so.notion.site/Creating-a-new-gallery-and-organizing-your-NFTs-b407a174a2ee44748bb9952abd803290';

function TitleSection() {
  const { createCollection } = useGalleryEditorContext();

  return (
    <TitleSectionWrapper align="center" justify="space-between">
      <TitleS>Collections</TitleS>
      <IconContainer
        variant="default"
        size="sm"
        icon={<CreateCollectionIcon />}
        onClick={createCollection}
      />
    </TitleSectionWrapper>
  );
}

type Props = {
  queryRef: CollectionSidebarQueryFragment$key;
};

export function CollectionSidebar({ queryRef }: Props) {
  const query = useFragment(
    graphql`
      fragment CollectionSidebarQueryFragment on Query {
        ...CollectionSearchQueryFragment
      }
    `,
    queryRef
  );

  return (
    <CollectionSidebarWrapper gap={8}>
      <TitleSection />
      <CollectionSearch queryRef={query} />
      <StyledIconsContainer justify="space-between">
        <InteractiveLink href={NOTION_DOCS_URL}>
          <a>
            <HelpIconContainer />
          </a>
        </InteractiveLink>
        <AutoGalleryToggle />
      </StyledIconsContainer>
    </CollectionSidebarWrapper>
  );
}

function HelpIconContainer() {
  const { floating, reference, getFloatingProps, getReferenceProps, floatingStyle } =
    useTooltipHover({
      placement: 'top-start',
    });

  return (
    <>
      <NewTooltip
        {...getFloatingProps()}
        style={floatingStyle}
        ref={floating}
        text="How to set up your gallery"
      />
      <StyledHelpIconContainer
        variant="blue"
        size="md"
        icon={<QuestionMarkIcon />}
        ref={reference}
        {...getReferenceProps()}
      />
    </>
  );
}

/**
 * For brand new galleries, display the Autogallery icon.
 * For non-empty galleries, display no icon.
 * If the Autogallery icon is clicked, display an `Undo` option.
 * For non-empty galleries, ensure `Undo` does not appear.
 */
function AutoGalleryToggle() {
  const { isEmptyGallery } = useGalleryEditorContext();

  const [variant, setVariant] = useState<'autogallery' | 'undo' | null>(null);

  useEffect(() => {
    if (isEmptyGallery) {
      setVariant('autogallery');
    }
  }, [isEmptyGallery]);

  if (variant === 'autogallery') {
    return <AutogalleryIconContainer onClick={() => setVariant('undo')} />;
  }

  if (variant === 'undo') {
    return <UndoIconContainer onClick={() => setVariant('autogallery')} />;
  }

  return null;
}

type IconContainerProps = {
  onClick: () => void;
};

function AutogalleryIconContainer({ onClick }: IconContainerProps) {
  const { floating, reference, getFloatingProps, getReferenceProps, floatingStyle } =
    useTooltipHover({
      placement: 'top-start',
    });

  const { setAutogeneratedGallery } = useGalleryEditorContext();
  const { step, dialogMessage, nextStep, handleClose } = useOnboardingDialogContext();

  return (
    <>
      <NewTooltip
        {...getFloatingProps()}
        style={{ ...floatingStyle, width: '162px', left: '56px' }}
        ref={floating}
        whiteSpace="pre-line"
        text={`Auto-generate a gallery based on your collections.\n\nFeel free to update it afterwards, and don't forget to save once you're done!`}
      />
      <StyledAutogalleryIconContainer
        onClick={() => {
          setAutogeneratedGallery();
          onClick();
        }}
        variant="blue"
        size="md"
        icon={<AdmireIcon />}
        ref={reference}
        {...getReferenceProps()}
      />
      {step === 7 && (
        <OnboardingDialog
          step={step}
          text={dialogMessage}
          onNext={nextStep}
          onClose={handleClose}
          options={{
            placement: 'left-start',
            positionOffset: 20,
            blinkingPosition: {
              right: 15,
              bottom: 12,
            },
          }}
        />
      )}
    </>
  );
}

function UndoIconContainer({ onClick }: IconContainerProps) {
  const { floating, reference, getFloatingProps, getReferenceProps, floatingStyle } =
    useTooltipHover({
      placement: 'top-start',
    });

  const { resetGalleryToEmptyState } = useGalleryEditorContext();

  return (
    <>
      <NewTooltip
        {...getFloatingProps()}
        style={{ ...floatingStyle, left: '100px' }}
        ref={floating}
        text="Reset to empty state"
      />
      <StyledUndoIconContainer
        onClick={() => {
          resetGalleryToEmptyState();
          onClick();
        }}
        variant="blue"
        size="md"
        icon={<UndoIcon />}
        ref={reference}
        {...getReferenceProps()}
      />
    </>
  );
}

